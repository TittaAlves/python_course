'''
Operadores aritméticos

+ : adissão
- : subtração
* : multiplicação
/ : divisão
// : divisão inteira
** : exponenciação
% : retorna o modulo
() : para alterar a precedência
'''

print(10*10)
print(10+10)
print(10-5)
print(10 / 2)
print(10 // 2)
print(2 ** 10)
print(10 % 5)
print(50 % 3)
print((5+2)*10)